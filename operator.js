// Operator Aritmatika
let a = 10;
let b = 5;
let tambah = a + b; // 15
let kurang = a - b; // 5
let kali = a * b;   // 50
let bagi = a / b;   // 2
let sisa = a % b;   // 0 

console.log("Penambahan: " + tambah);

// Operator Perbandingan (akan menghasilkan boolean)
let lebihDari = a > b;  
let samaDengan = a === b; 

console.log("10 lebih dari 5: " + lebihDari);

let x = true;
let y = false;
let dan = x && y; // false (AND)
let atau = x || y; // true (OR)
let tidak = !x;   // false (NOT)

console.log("True DAN False: " + dan);